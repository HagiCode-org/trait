import assert from "node:assert/strict"
import fs from "node:fs/promises"
import path from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"
import { load } from "js-yaml"

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url))
const projectRoot = path.resolve(scriptDirectory, "..")
const defaultLocalesRoot = path.join(projectRoot, "src/i18n/locales-source")
const defaultGeneratedRoot = path.join(projectRoot, "src/i18n/locales")
const expectedSourceLocales = [
  "en-US",
  "zh-CN",
  "zh-Hant",
  "ja-JP",
  "ko-KR",
  "de-DE",
  "fr-FR",
  "es-ES",
  "pt-BR",
  "ru-RU",
]
const expectedNamespaces = ["ui"]
const runtimeLocaleBySourceLocale = {
  "en-US": "en",
  "zh-CN": "zh-CN",
  "zh-Hant": "zh-Hant",
  "ja-JP": "ja-JP",
  "ko-KR": "ko-KR",
  "de-DE": "de-DE",
  "fr-FR": "fr-FR",
  "es-ES": "es-ES",
  "pt-BR": "pt-BR",
  "ru-RU": "ru-RU",
}

function isPlainObject(value) {
  return typeof value === "object" && value !== null && !Array.isArray(value)
}

function normalizeNames(values) {
  return [...values].sort((left, right) => left.localeCompare(right))
}

function toPosixPath(filePath) {
  return path.relative(projectRoot, filePath).split(path.sep).join("/")
}

function exportNameForLocale(runtimeLocale) {
  if (runtimeLocale === "en") {
    return "enMessages"
  }

  if (runtimeLocale === "zh-CN") {
    return "zhCnMessages"
  }

  return `${runtimeLocale.replace(/-([a-zA-Z])/g, (_, letter) => letter.toUpperCase()).replace(/^[a-z]/, (letter) => letter.toLowerCase())}Messages`
}

function formatJson(value) {
  return JSON.stringify(value, null, 2)
}

function formatGeneratedModule(runtimeLocale, messages) {
  const exportName = exportNameForLocale(runtimeLocale)
  const body = formatJson(messages)

  if (runtimeLocale === "en") {
    const typeShape = Object.keys(messages)
      .map((key) => `  ${JSON.stringify(key)}: string`)
      .join("\n")

    return [
      "/* This file is generated by scripts/generate-i18n-resources.mjs. Do not edit by hand. */",
      "",
      "export type LocaleMessages = {",
      typeShape,
      "}",
      "",
      `export const ${exportName}: LocaleMessages = ${body}`,
      "",
    ].join("\n")
  }

  return [
    "/* This file is generated by scripts/generate-i18n-resources.mjs. Do not edit by hand. */",
    "",
    'import type { LocaleMessages } from "./en"',
    "",
    `export const ${exportName}: LocaleMessages = ${body}`,
    "",
  ].join("\n")
}

async function listDirectoryNames(directoryPath) {
  const entries = await fs.readdir(directoryPath, { withFileTypes: true })
  return normalizeNames(entries.filter((entry) => entry.isDirectory()).map((entry) => entry.name))
}

async function readLocaleNamespace(localesRoot, locale, namespace) {
  const localeDirectory = path.join(localesRoot, locale)
  const ymlPath = path.join(localeDirectory, `${namespace}.yml`)
  const yamlPath = path.join(localeDirectory, `${namespace}.yaml`)
  const ymlExists = await fs.access(ymlPath).then(() => true).catch(() => false)
  const yamlExists = await fs.access(yamlPath).then(() => true).catch(() => false)

  assert(!(ymlExists && yamlExists), `Found both .yml and .yaml for ${locale}/${namespace}`)
  assert(ymlExists || yamlExists, `Missing YAML namespace file for ${locale}/${namespace}`)

  const filePath = ymlExists ? ymlPath : yamlPath
  const data = load(await fs.readFile(filePath, "utf8"))
  assert(isPlainObject(data), `${toPosixPath(filePath)} must be a top-level mapping`)
  return data
}

function collectScalars(value, prefix = "", scalars = new Map()) {
  if (isPlainObject(value)) {
    for (const [key, child] of Object.entries(value)) {
      collectScalars(child, prefix ? `${prefix}.${key}` : key, scalars)
    }
    return scalars
  }

  assert.equal(typeof value, "string", `${prefix} must be a string`)
  scalars.set(prefix, value)
  return scalars
}

function collectPlaceholders(value) {
  return normalizeNames([...value.matchAll(/\{\{\s*([A-Za-z0-9_]+)\s*\}\}/g)].map((match) => match[1]))
}

function validateLocaleShape(baseMessages, targetMessages, locale) {
  const baseScalars = collectScalars(baseMessages)
  const targetScalars = collectScalars(targetMessages)
  assert.deepEqual([...targetScalars.keys()], [...baseScalars.keys()], `${locale}/ui.yml key paths must match en-US/ui.yml`)

  for (const [keyPath, baseValue] of baseScalars) {
    const targetValue = targetScalars.get(keyPath)
    assert.deepEqual(
      collectPlaceholders(targetValue),
      collectPlaceholders(baseValue),
      `${locale}/ui.yml placeholder mismatch at ${keyPath}`,
    )
  }
}

async function loadSourceResources(options = {}) {
  const localesRoot = path.resolve(options.localesRoot ?? defaultLocalesRoot)
  const generatedRoot = path.resolve(options.generatedRoot ?? defaultGeneratedRoot)
  const expectedLocales = options.expectedLocales ?? expectedSourceLocales
  const actualLocales = await listDirectoryNames(localesRoot)
  assert.deepEqual(actualLocales, normalizeNames(expectedLocales), `${toPosixPath(localesRoot)} locale directories must match hagi18n.yaml`)

  const resources = {}

  for (const locale of expectedLocales) {
    const localeDirectory = path.join(localesRoot, locale)
    const sourceEntries = await fs.readdir(localeDirectory, { withFileTypes: true })
    const actualNamespaces = normalizeNames(
      sourceEntries
        .filter((entry) => entry.isFile() && /\.(?:ya?ml)$/u.test(entry.name))
        .map((entry) => entry.name.replace(/\.(?:ya?ml)$/u, "")),
    )
    assert.deepEqual(actualNamespaces, expectedNamespaces, `${locale} YAML namespaces must match expected Trait namespaces`)
    resources[locale] = await readLocaleNamespace(localesRoot, locale, "ui")
  }

  for (const locale of expectedLocales.filter((locale) => locale !== "en-US")) {
    validateLocaleShape(resources["en-US"], resources[locale], locale)
  }

  return { generatedRoot, localesRoot, resources, expectedLocales }
}

async function buildGeneratedFiles(options = {}) {
  const { generatedRoot, resources, expectedLocales } = await loadSourceResources(options)
  const files = new Map()

  for (const sourceLocale of expectedLocales) {
    const runtimeLocale = runtimeLocaleBySourceLocale[sourceLocale]
    assert(runtimeLocale, `No Trait runtime locale mapping configured for ${sourceLocale}`)
    files.set(path.join(generatedRoot, `${runtimeLocale}.ts`), formatGeneratedModule(runtimeLocale, resources[sourceLocale]))
  }

  return { generatedRoot, files, localeCount: expectedLocales.length, namespaceCount: expectedNamespaces.length }
}

export async function generateI18nResources(options = {}) {
  const result = await buildGeneratedFiles(options)
  await fs.rm(result.generatedRoot, { recursive: true, force: true })
  await fs.mkdir(result.generatedRoot, { recursive: true })

  for (const [filePath, content] of [...result.files.entries()].sort(([left], [right]) => left.localeCompare(right))) {
    await fs.writeFile(filePath, content, "utf8")
  }

  return result
}

export async function verifyGeneratedI18nResources(options = {}) {
  const result = await buildGeneratedFiles(options)
  const errors = []

  for (const [filePath, expectedContent] of [...result.files.entries()].sort(([left], [right]) => left.localeCompare(right))) {
    const actualContent = await fs.readFile(filePath, "utf8").catch(() => null)
    if (actualContent !== expectedContent) {
      errors.push(`${toPosixPath(filePath)} is stale; rerun npm run i18n:generate`)
    }
  }

  const generatedEntries = await fs.readdir(result.generatedRoot, { withFileTypes: true }).catch(() => [])
  const actualFiles = normalizeNames(generatedEntries.filter((entry) => entry.isFile() && entry.name.endsWith(".ts")).map((entry) => entry.name))
  const expectedFiles = normalizeNames([...result.files.keys()].map((filePath) => path.basename(filePath)))
  if (actualFiles.length > 0) {
    try {
      assert.deepEqual(actualFiles, expectedFiles)
    } catch {
      errors.push(`${toPosixPath(result.generatedRoot)} files do not match expected generated Trait locale modules`)
    }
  }

  assert.equal(errors.length, 0, errors.join("\n"))
  return result
}

function parseCliArgs(argv) {
  const options = {}

  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index]
    switch (argument) {
      case "--check":
        options.check = true
        break
      case "--locales-root":
        options.localesRoot = argv[index + 1]
        index += 1
        break
      case "--generated-root":
        options.generatedRoot = argv[index + 1]
        index += 1
        break
      default:
        throw new Error(`Unknown argument: ${argument}`)
    }
  }

  return options
}

async function main() {
  const options = parseCliArgs(process.argv.slice(2))

  if (options.check) {
    const result = await verifyGeneratedI18nResources(options)
    console.log(`Verified generated Trait i18n resources for ${result.localeCount} locales and ${result.namespaceCount} namespace.`)
    return
  }

  const result = await generateI18nResources(options)
  console.log(`Generated Trait i18n resources in ${toPosixPath(result.generatedRoot)} for ${result.localeCount} locales and ${result.namespaceCount} namespace.`)
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch((error) => {
    console.error(error instanceof Error ? error.message : error)
    process.exitCode = 1
  })
}
